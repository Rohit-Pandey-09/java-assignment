
public class quesFour {
public static void main(String[] args) {
// i) both the conditions 'a < 50' and 'a < b' are true.

int a = 55;
int b = 70;
System.out.print(a < 50 && a < b);


//ii) at least one of the conditions 'a < 50' or 'a < b' is true.

 a = 55;
 b = 70;
System.out.print(a < 50 || a < b);

}
}